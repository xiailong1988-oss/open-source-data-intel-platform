const createSpatialLink = (kind: DashboardSpatialLinkKind, id: string, title: string): DashboardSpatialLink => ({
  id,
  title,
  kind,
})

const createLayerMetric = (
  heat: number,
  objectCount: number,
  alertCount: number,
  eventCount: number,
  mainTopic: string,
): DashboardSpatialRegionMetric => ({
  heat,
  objectCount,
  alertCount,
  eventCount,
  mainTopic,
})

const createHeadline = (id: string, label: string, value: string, hint: string): DashboardSpatialHeadline => ({
  id,
  label,
  value,
  hint,
})

const createBeijingRegion = (
  id: string,
  adcode: string,
  name: string,
  center: [number, number],
  summary: string,
  emphasis: string,
  todayFocus: boolean,
  metrics: {
    综合: [number, number, number, number, string]
    风险预警: [number, number, number, number, string]
    重点事件: [number, number, number, number, string]
    专题研判: [number, number, number, number, string]
  },
): DashboardSpatialRegion => ({
  id,
  adcode,
  name,
  center,
  summary,
  emphasis,
  todayFocus,
  layerMetrics: {
    综合: createLayerMetric(...metrics.综合),
    风险预警: createLayerMetric(...metrics.风险预警),
    重点事件: createLayerMetric(...metrics.重点事件),
    专题研判: createLayerMetric(...metrics.专题研判),
  },
})

