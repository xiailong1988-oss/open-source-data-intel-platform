const createLink = (
  kind: DashboardCockpitLinkKind,
  id: string,
  title: string,
  path?: string,
  query?: Record<string, string>,
): DashboardCockpitLink => ({
  id,
  title,
  kind,
  path,
  query,
})

const createHeadline = (id: string, label: string, value: string, hint: string): DashboardCockpitHeadline => ({
  id,
  label,
  value,
  hint,
})

const createZone = (
  id: string,
  name: string,
  center: [number, number],
  weight: number,
  heat: number,
  alertCount: number,
  eventCount: number,
  emphasis: string,
  description: string,
  keyword: string,
  featuredPointIds: string[],
): DashboardCockpitZone => ({
  id,
  name,
  center,
  weight,
  heat,
  alertCount,
  eventCount,
  emphasis,
  description,
  keyword,
  featuredPointIds,
})

const createRegion = (
  id: string,
  name: string,
  shortName: string,
  type: DashboardCockpitRegion['type'],
  code: string,
  center: [number, number],
  zoom: number,
  bounds: [[number, number], [number, number]],
  order: number,
  description: string,
  emphasis: string,
  areaKeywords: string[],
  zoneIds: string[],
): DashboardCockpitRegion => ({
  id,
  name,
  shortName,
  type,
  code,
  center,
  zoom,
  bounds,
  order,
  enabled: true,
  description,
  emphasis,
  areaKeywords,
  zoneIds,
})

